#Ini merupakan salah satu penerapan ADT dalam kehidupan sehari-hari
#contohnya pada luas lingkaran seperti yang ada dibawah ini

phi = 3.14
#phi atau Ï€ (22/7 atau 3.14) merupakan sebuah ketetapan dalam rumus sebuah lingkaran

r = float(input("Masukkan panjang jari-jari lingkaran: "))
#r merupakan jari-jari dari sebuah lingkaran atau setengah dari diameter sebuah lingkaran

luas = phi * (r**2)
#karena luas dari sebuah lingkaran adalah phi*r*r maka dapat dipersingkat menjadi phi*r2

print("Luas lingkaran adalah: "+ str(luas))
